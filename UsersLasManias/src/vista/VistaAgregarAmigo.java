    public void actualizarVista(String _nombreUsuario){
        usuarios = controlador.buscarUsuarios(_nombreUsuario);
        listModelBusqueda = new DefaultListModel();
        llenarLista();

        listaBusqueda.setModel(listModelBusqueda);
    }
    public void actualizarVista(){
        usuarios = controlador.listaUsuarios();
        listModelBusqueda = new DefaultListModel();
        llenarLista();

        listaBusqueda.setModel(listModelBusqueda);
    }
    
    private void llenarLista(){
        int i = 0;
        for (Usuario usuario : usuarios) {
            listModelBusqueda.add(i, usuario);
            i++;
        }
    }
